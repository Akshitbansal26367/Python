# Program to create a set
s = {10, 20, 15, 18}
print("The set is", s)

# Program to iterate over sets
s1 = {10, 4, 6, 1, 66, 7}
print("The set iteration is", end=" ")
for a in s1:
    print(a, end='\t')

# Program to add a member in a set
s2 = {20, 30, 10, 5, 76}
print("\n\nThe initial set is", s2)
s2.add(3)
print("Updated set is", s2)
